package ReservarInstalacionComoSocio;

public class InstalacionEntity {
	//Instalaciones
	Integer idInstalacion, preciohora;
	String nombre;
	
	public Integer getIdInstalacion() {
		return idInstalacion;
	}
	public void setIdInstalacion(Integer idInstalacion) {
		this.idInstalacion = idInstalacion;
	}
	public Integer getpreciohora() {
		return preciohora;
	}
	public void setpreciohora(Integer preciohora) {
		this.preciohora = preciohora;
	}
	public String getnombre() {
		return nombre;
	}
	public void setnombre(String nombre) {
		this.nombre = nombre;
	} 

	

}
